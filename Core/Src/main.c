void LEDTask_lab1(void *pvParameters){
	struct AMessage xRxedStructure;

	for(;;){
		if( xStructQueue != NULL )
			   {
			      /* Receive a message from the created queue to hold complex struct AMessage
			      structure.  Block for 10 ticks if a message is not immediately available.
			      The value is read into a struct AMessage variable, so after calling
			      xQueueReceive() xRxedStructure will hold a copy of xMessage. */
			      if( xQueueReceive( xStructQueue,
			                         (struct Amessage*)&( xRxedStructure ),
			                         ( TickType_t ) 10 ) == pdPASS )
			      {
			         /* xRxedStructure now contains a copy of xMessage. */
			    	  if(xRxedStructure.ucData == 'A'){
			    		  HAL_GPIO_WritePin(led_green_GPIO_Port, led_green_Pin, GPIO_PIN_SET);
			    		  HAL_GPIO_WritePin(led_red_GPIO_Port, led_red_Pin, GPIO_PIN_RESET);
			    		  vTaskDelay(2000);
			    		  HAL_GPIO_WritePin(led_red_GPIO_Port, led_red_Pin, GPIO_PIN_SET);
			    		  HAL_GPIO_WritePin(led_green_GPIO_Port, led_green_Pin, GPIO_PIN_RESET);
			    		  vTaskDelay(2000);
			    	  }else if(xRxedStructure.ucData == 'B'){
			    		  HAL_GPIO_WritePin(led_orange_GPIO_Port, led_orange_Pin, GPIO_PIN_SET);
			    		  vTaskDelay(1000);
			    		  HAL_GPIO_WritePin(led_orange_GPIO_Port, led_orange_Pin, GPIO_PIN_RESET);
			    		  vTaskDelay(1000);
			    	  		}
			      }
			   }
	}
}

void ButtonTask(void *pvParameters){

	int status = 0;
	for(;;){
		if(HAL_GPIO_ReadPin(btn_GPIO_ReadPin, btn_blue_Pin)){
			status++;
			if(status%2 == 0)
				xMessage.ucData='A';
			else
				xMessage.ucData='B';
			xQueueSend(xStructQueue, ( void * ) &xMessage, ( TickType_t ) 0 );
			vTaskDelay(200);
		}
	}
}

